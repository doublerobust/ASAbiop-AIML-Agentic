#!/usr/bin/env python3
"""Check that R and Python TC-012 outputs agree on identical data."""
import json
import sys
from pathlib import Path

HERE = Path(__file__).parent
R_OUT = HERE / "tc012-r-output.json"
PY_OUT = HERE / "tc012-py-output.json"

TOLERANCE_HR = 0.05    # HR / CI tolerance
TOLERANCE_PVAL = 0.1   # interaction p-value tolerance

with open(R_OUT) as f:
    r = json.load(f)
with open(PY_OUT) as f:
    py = json.load(f)

def check_val(label, a, b, tol=TOLERANCE_HR):
    if a is None or b is None:
        return "MISSING"
    diff = abs(a - b)
    ok = "✅" if diff <= tol else "❌"
    return f"{a:<10} {b:<10} {diff:<10.5f} {ok}"

results = []
all_pass = True

# Overall
for k in ['hr', 'ci_lower', 'ci_upper', 'n_experimental', 'n_control',
           'events_experimental', 'events_control']:
    chk = check_val(k, py['overall'].get(k), r['overall'].get(k))
    if '❌' in chk:
        all_pass = False
    results.append(f"  Overall {k:<25} {chk}")

# Subgroups
for ps, rs in zip(py['subgroups'], r['subgroups']):
    for k in ['hr', 'ci_lower', 'ci_upper']:
        chk = check_val(k, ps.get(k), rs.get(k))
        if '❌' in chk:
            all_pass = False
        results.append(f"  {ps['subgroup']:<30} {k:<12} {chk}")
    results.append("")

# Interaction p-values
for var in py['interaction_pvalues']:
    pp = py['interaction_pvalues'].get(var)
    rp = r['interaction_pvalues'].get(var)
    chk = check_val(var, pp, rp, tol=TOLERANCE_PVAL)
    if '❌' in chk:
        all_pass = False
    results.append(f"  Interaction {var:<20} {chk}")

# Summary
print("=" * 75)
print("TC-012 CROSS-LANGUAGE COMPARISON")
print(f"R script:      {R_OUT}")
print(f"Python script: {PY_OUT}")
print(f"Data source:   Identical CSV (generated by R)")
print("=" * 75)
print()
print("\n".join(results))
print()
print("=" * 75)
if all_pass:
    print(f"✅ ALL {len(results)} CHECKS PASSED within tolerance")
else:
    print("❌ SOME CHECKS FAILED")
    sys.exit(1)
print("=" * 75)
