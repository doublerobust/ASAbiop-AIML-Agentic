ERROR_FUNCTIONS = {
    "n_count": inject_error_n_count,
    "missing_category": inject_error_missing_category,
    "denominator": inject_error_denominator,
    "sorting": inject_error_sorting,
    "typo": inject_error_typo,
    "method": inject_error_method,
    "population": inject_error_population,
}


def inject_single_error(tfl_data: dict, error_spec: dict) -> dict:
    """Apply a single error injection based on a spec dict.

    Args:
        tfl_data: Clean TFL output.
        error_spec: Dict with 'type' key and function-specific parameters.

    Returns:
        Modified copy of tfl_data with the error injected.
    """
    error_type = error_spec["type"]
    func = ERROR_FUNCTIONS.get(error_type)
    if func is None:
        raise ValueError(f"Unknown error type: {error_type}")

    # Build kwargs from error_spec (exclude 'type' and metadata fields)
    kwargs = {k: v for k, v in error_spec.items()
              if k not in ("type", "error_id", "class", "description", "tfl_id",
                           "correction", "sap_reference", "location",
                           "description_method", "method_description")}

    # Pass tfl_id separately
    tfl_id = error_spec.get("tfl_id", "unknown")
    return func(tfl_data, tfl_id, **kwargs)


def inject_all_errors(tfl_package: dict, error_catalog: dict,
                      variant_id: str = "v1") -> dict:
    """Inject all errors from a catalog into a TFL package.

    Args:
        tfl_package: Dict mapping tfl_id -> clean TFL output.
        error_catalog: Parsed error_catalog.yaml.
        variant_id: Variant identifier to select error configuration.

    Returns:
        Dict with:
          'tfl_package': modified TFL outputs
          'ground_truth': list of error specs that were applied
          'clean_tfls': list of tfl_ids with no errors injected
    """
    variant = error_catalog.get("variants", {}).get(variant_id, {})
    errors = variant.get("errors", [])

    # Handle reference to default_errors by name
    if isinstance(errors, str):
        errors = error_catalog.get(errors, [])
    elif not errors:
        errors = error_catalog.get("default_errors", [])

    injected_package = copy.deepcopy(tfl_package)
    applied_errors = []
    error_tfl_ids = set()

    for error_spec in errors:
        tfl_id = error_spec["tfl_id"]
        error_tfl_ids.add(tfl_id)

        if tfl_id not in injected_package:
            # TFL not in package — skip
            continue

        injected_package[tfl_id] = inject_single_error(
            injected_package[tfl_id], error_spec
        )
        applied_errors.append(error_spec)

    clean_tfls = [tid for tid in injected_package if tid not in error_tfl_ids]

    return {
        "tfl_package": injected_package,
        "ground_truth": applied_errors,
        "clean_tfls": clean_tfls,
    }


