"""Error Injection Framework for TC-005 (TFL Package QC Review)

Injects controlled errors into clean TFL outputs generated by Level 1
ground truth scripts. Each error function modifies a specific aspect
of a TFL output to simulate a realistic programming mistake.

Error taxonomy follows the A/B/C classification:
  Class A (Critical) — regulatory rejection risk or patient harm
  Class B (Major)   — correction required before submission
  Class C (Minor)   — cosmetic or stylistic

Usage:
  from error_injection import inject_all_errors, load_error_catalog

  catalog = load_error_catalog()
  injected = inject_all_errors(clean_tfl_package, catalog, variant_id="v1")
"""

import copy
import json
import random
from pathlib import Path
from typing import Any, Dict, List, Optional

import yaml


# --------------------------------------------------------------------
# Error Catalog Loader
# --------------------------------------------------------------------

def load_error_catalog(path: Optional[str] = None) -> dict:
    """Load the error catalog from YAML.

    Args:
        path: Path to error_catalog.yaml. Defaults to the file next to this module.

    Returns:
        Parsed error catalog dict.
    """
    if path is None:
        path = Path(__file__).parent / "error_catalog.yaml"
    with open(path) as f:
        return yaml.safe_load(f)


