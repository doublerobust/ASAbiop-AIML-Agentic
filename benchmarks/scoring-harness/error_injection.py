def inject_error_n_count(tfl_data: dict, tfl_id: str, delta: int = -2) -> dict:
    """Reduce the N count for a specified arm in a table.

    Class B error — the table is internally consistent but uses the
    wrong population (e.g., safety instead of ITT).

    Args:
        tfl_data: The clean TFL output dict.
        tfl_id: TFL identifier (e.g., "table-001").
        delta: How much to change N by (negative reduces).

    Returns:
        Modified copy of tfl_data.
    """
    data = copy.deepcopy(tfl_data)
    # Modify the experimental arm N
    if "summary" in data:
        for row in data["summary"]:
            if row.get("label") == "N" and "experimental" in row:
                row["experimental"] = int(row["experimental"]) + delta
    elif "arm_experimental" in data and "n_total" in data["arm_experimental"]:
        data["arm_experimental"]["n_total"] = int(data["arm_experimental"]["n_total"]) + delta
    # Also adjust any percentages that depend on N
    if "summary" in data:
        for row in data["summary"]:
            if "pct_experimental" in row and "n_experimental" in row and row.get("experimental"):
                n = int(row["experimental"])
                if n > 0:
                    row["pct_experimental"] = round(100.0 * n / (int(row.get("denominator_experimental", row["experimental"])) + delta), 1)
    return data


def inject_error_missing_category(tfl_data: dict, tfl_id: str, category_field: str,
                                  category_value: str) -> dict:
    """Remove a category from a categorical variable breakdown.

    Class B error — a demographic category (e.g., "Asian") is missing.

    Args:
        tfl_data: The clean TFL output dict.
        tfl_id: TFL identifier.
        category_field: Field name (e.g., "race").
        category_value: Category to remove (e.g., "Asian").

    Returns:
        Modified copy of tfl_data.
    """
    data = copy.deepcopy(tfl_data)
    # Remove from categorical_rows, detail_rows, or similar structures
    for key in ["categorical_rows", "detail_rows", "rows", "categories"]:
        if key in data:
            data[key] = [r for r in data[key]
                         if str(r.get("category", r.get("label", r.get("value", "")))) != category_value]
    # Also check nested arm structure
    for arm_key in ["arm_control", "arm_experimental"]:
        if arm_key in data and "categories" in data[arm_key]:
            data[arm_key]["categories"] = [r for r in data[arm_key]["categories"]
                                           if str(r.get("category", r.get("label", ""))) != category_value]
    return data


def inject_error_denominator(tfl_data: dict, tfl_id: str, old_denom: int,
                              new_denom: int) -> dict:
    """Replace denominator in n/N calculations.

    Class A error — using evaluable N instead of ITT N for ORR.

    Args:
        tfl_data: The clean TFL output dict.
        tfl_id: TFL identifier.
        old_denom: Original denominator value.
        new_denom: Incorrect denominator value to inject.

    Returns:
        Modified copy of tfl_data.
    """
    data = copy.deepcopy(tfl_data)
    # Recalculate all percentages with the wrong denominator
    for key in ["summary", "overall", "result_groups", "subgroups"]:
        if key not in data:
            continue
        items = data[key] if isinstance(data[key], list) else [data[key]]
        for item in items:
            for field in list(item.keys()):
                if field.startswith("pct_") or field.endswith("_pct"):
                    n_field = field.replace("pct_", "n_").replace("_pct", "_n")
                    if n_field in item:
                        n = float(item[n_field])
                        item[field] = round(100.0 * n / new_denom, 1)
                if field == "denominator" and item[field] == old_denom:
                    item[field] = new_denom
            if "denominator" in item and item.get("denominator") == old_denom:
                item["denominator"] = new_denom
    return data


def inject_error_sorting(tfl_data: dict, tfl_id: str, sort_field: str = "soc") -> dict:
    """Change sort order from frequency to alphabetical.

    Class C error — SOC sorted alphabetically instead of by AE frequency.

    Args:
        tfl_data: The clean TFL output dict.
        tfl_id: TFL identifier.
        sort_field: Field to re-sort.

    Returns:
        Modified copy of tfl_data.
    """
    data = copy.deepcopy(tfl_data)
    for key in ["detail_rows", "rows", "soc_rows"]:
        if key in data and isinstance(data[key], list):
            # Sort alphabetically by the first string field (category/soc/pt)
            def sort_key(row):
                for f in ["soc", "category", "pt", "label", "preferred_term"]:
                    if f in row:
                        return str(row[f])
                return str(row.get("label", ""))
            data[key] = sorted(data[key], key=sort_key)
    return data


def inject_error_typo(tfl_data: dict, tfl_id: str, field: str,
                       old_value: str, new_value: str) -> dict:
    """Inject a spelling error in a text field.

    Class C error — a medical term is misspelled.

    Args:
        tfl_data: The clean TFL output dict.
        tfl_id: TFL identifier.
        field: Field name containing the value.
        old_value: Correct value.
        new_value: Misspelled value.

    Returns:
        Modified copy of tfl_data.
    """
    data = copy.deepcopy(tfl_data)

    def _replace_in_obj(obj):
        if isinstance(obj, dict):
            for k, v in obj.items():
                if v == old_value:
                    obj[k] = new_value
                else:
                    _replace_in_obj(v)
        elif isinstance(obj, list):
            for i, v in enumerate(obj):
                if v == old_value:
                    obj[i] = new_value
                else:
                    _replace_in_obj(v)

    _replace_in_obj(data)
    return data


def inject_error_method(tfl_data: dict, tfl_id: str, description: str = "",
                         method_description: str = "", **kwargs) -> dict:
    """Change the statistical method description.

    Class A error — e.g., using wrong censoring indicator for KM curve.

    Args:
        tfl_data: The clean TFL output dict.
        tfl_id: TFL identifier.
        description: Description of the method change (stored in metadata).

    Returns:
        Modified copy of tfl_data.
    """
    data = copy.deepcopy(tfl_data)
    # Flip the censoring indicator logic
    if "censoring_indicator" in data:
        data["censoring_indicator"] = 1 - data["censoring_indicator"]
        data["metadata"] = data.get("metadata", {})
        data["metadata"]["error_injected"] = description
    elif "method" in data:
        data["method"] = "1-CNSR (wrong)"
        data["metadata"] = data.get("metadata", {})
        data["metadata"]["error_injected"] = description
    # For KM curve data, flip event/censor flags
    if "curve_data" in data:
        for point in data["curve_data"]:
            if "event" in point:
                point["event"] = 1 - point.get("event", 0)
    return data


def inject_error_population(tfl_data: dict, tfl_id: str,
                             old_pop: str = "ITT", new_pop: str = "SAFETY") -> dict:
    """Change the population filter applied.

    Class A error — using SAFETY population instead of ITT.

    Args:
        tfl_data: The clean TFL output dict.
        tfl_id: TFL identifier.
        old_pop: Original population label.
        new_pop: Incorrect population label.

    Returns:
        Modified copy of tfl_data.
    """
    data = copy.deepcopy(tfl_data)
    # Replace population references
    def _replace_pop(obj):
        if isinstance(obj, dict):
            for k, v in list(obj.items()):
                if v == old_pop and k in ("population", "pop", "analysis_population"):
                    obj[k] = new_pop
                else:
                    _replace_pop(v)
        elif isinstance(obj, list):
            for v in obj:
                _replace_pop(v)

    _replace_pop(data)
    # Also adjust N counts to the safety population (typically smaller)
    if "summary" in data and isinstance(data["summary"], list):
        for row in data["summary"]:
            if isinstance(row, dict) and row.get("label") == "N":
                for arm in ["control", "experimental"]:
                    if arm in row:
                        row[arm] = int(row[arm]) - 2  # Simulate safety < ITT
    return data


