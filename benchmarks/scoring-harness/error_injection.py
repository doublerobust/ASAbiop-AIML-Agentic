# Scoring for TC-005
# --------------------------------------------------------------------

def score_tc005(agent_output: dict, ground_truth: dict) -> dict:
    """Score a TC-005 QC review submission.

    Auto-scorable components:
      - Error detection (true positives)
      - Error classification (A/B/C)
      - Error location accuracy
      - Cross-TFL consistency checks

    LLM-as-judge and human review components are handled separately.

    Args:
        agent_output: The agent's QC review JSON.
        ground_truth: The ground truth error list.

    Returns:
        Score dict with component breakdown.
    """
    gt_errors = {e["error_id"]: e for e in ground_truth}
    agent_errors = {e.get("error_id", ""): e for e in agent_output.get("discrepancies", [])}

    # True positives
    tp_errors = []
    fp_errors = []
    fn_errors = []

    for eid, ag_err in agent_errors.items():
        if eid in gt_errors:
            tp_errors.append((eid, ag_err, gt_errors[eid]))
        else:
            fp_errors.append(ag_err)

    for eid in gt_errors:
        if eid not in agent_errors:
            fn_errors.append(gt_errors[eid])

    # Error detection score
    n_gt = len(gt_errors)
    n_tp = len(tp_errors)
    n_fp = len(fp_errors)
    n_fn = len(fn_errors)

    detection_score = n_tp / n_gt if n_gt > 0 else 1.0
    fp_penalty = min(n_fp * 0.05, 0.20)
    detection_score = max(0.0, detection_score - fp_penalty)

    # Classification score
    class_correct = 0
    for eid, ag_err, gt_err in tp_errors:
        if ag_err.get("class", "").upper() == gt_err.get("class", "").upper():
            class_correct += 1
    classification_score = class_correct / n_tp if n_tp > 0 else 0.0

    # Location accuracy (fuzzy match)
    location_score = 0.0
    for eid, ag_err, gt_err in tp_errors:
        ag_loc = str(ag_err.get("location", "")).lower()
        gt_loc = str(gt_err.get("location", "")).lower()
        # Simple substring match
        if ag_loc and gt_loc and (ag_loc in gt_loc or gt_loc in ag_loc):
            location_score += 1.0
        elif ag_loc and gt_loc:
            # Token overlap
            ag_tokens = set(ag_loc.split())
            gt_tokens = set(gt_loc.split())
            overlap = len(ag_tokens & gt_tokens)
            if overlap > 0:
                location_score += overlap / max(len(ag_tokens | gt_tokens), 1)
    location_score = location_score / n_tp if n_tp > 0 else 0.0

    # Cross-TFL checks
    ag_checks = agent_output.get("cross_tfl_checks", [])
    gt_checks = ground_truth  # ground truth includes expected cross-TFL checks
    check_score = 0.0
    expected_checks = ["N-count consistency"]
    for check in ag_checks:
        if any(ec.lower() in str(check.get("check", "")).lower() for ec in expected_checks):
            check_score += 1.0
    check_score = min(check_score / len(expected_checks), 1.0) if expected_checks else 1.0

    # Composite auto-score (60% of total)
    auto_score = (
        0.30 * detection_score +
        0.15 * classification_score +
        0.10 * location_score +
        0.05 * check_score
    )

    return {
        "test_case_id": "TC-005",
        "score": round(auto_score / 0.60, 4),  # Normalized to auto-score fraction
        "component_scores": {
            "error_detection": round(detection_score, 4),
            "error_classification": round(classification_score, 4),
            "error_location": round(location_score, 4),
            "cross_tfl_checks": round(check_score, 4),
            "true_positives": n_tp,
            "false_positives": n_fp,
            "false_negatives": n_fn,
            "fp_penalty": round(fp_penalty, 4),
        },
        "agent_language": agent_output.get("language", "unknown"),
        "variant_id": agent_output.get("variant_id"),
        "note": "Auto-scored components only (60%). LLM-judge (25%) and human review (15%) scored separately.",
    }
